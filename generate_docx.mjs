/** Надпись над таблицей: «Таблица X.X» — над правым верхним углом */
const tableCaption = (label) => new Paragraph({
  children: [new TextRun({ text: label, font: FONT, size: PT12 })],
  alignment: AlignmentType.RIGHT,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Название таблицы — по центру */
const tableTitle = (title) => new Paragraph({
  children: [new TextRun({ text: title, font: FONT, size: PT12, bold: true })],
  alignment: AlignmentType.CENTER,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

const headerCell = (text, w) => new TableCell({
  children: [new Paragraph({
    children: [new TextRun({ text, font: FONT, size: PT12, bold: true })],
    alignment: AlignmentType.CENTER,
    spacing: { line: LINE_SINGLE, after: 0, before: 0 },
  })],
  width: w ? { size: w, type: WidthType.PERCENTAGE } : undefined,
  shading: { type: ShadingType.SOLID, color: 'D9E1F2' },
});

const dataCell = (text, align = AlignmentType.LEFT) => new TableCell({
  children: [new Paragraph({
    children: [new TextRun({ text, font: FONT, size: PT12 })],
    alignment: align,
    spacing: { line: LINE_SINGLE, after: 0, before: 0 },
  })],
});

const codeCell = (text) => new TableCell({
  children: [new Paragraph({
    children: [new TextRun({ text, font: 'Courier New', size: PT12 - 2 })],
    alignment: AlignmentType.LEFT,
    spacing: { line: LINE_SINGLE, after: 0, before: 0 },
  })],
});

