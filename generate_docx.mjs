// ─── СТИЛИ АБЗАЦЕВ ───────────────────────────────────────────────────────────

/** Обычный абзац с красной строкой */
const para = (text, bold = false, italic = false) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12, bold, italics: italic })],
  alignment: AlignmentType.JUSTIFIED,
  indent: { firstLine: INDENT_FIRST },
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Абзац без отступа */
const paraNoIndent = (text, bold = false, center = false) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12, bold })],
  alignment: center ? AlignmentType.CENTER : AlignmentType.JUSTIFIED,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Центрированный абзац */
const paraCenter = (text, bold = false, italic = false) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12, bold, italics: italic })],
  alignment: AlignmentType.CENTER,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Правый абзац */
const paraRight = (text) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12 })],
  alignment: AlignmentType.RIGHT,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Пустая строка */
const emptyLine = () => new Paragraph({
  children: [new TextRun({ text: '', font: FONT, size: PT12 })],
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/**
 * Заголовок раздела (ПРОПИСНЫЕ, ПОЛУЖИРНЫЙ, 12пт, по левому краю)
 * Каждый раздел начинается с новой страницы.
 */
const h1 = (text) => new Paragraph({
  children: [new TextRun({ text: text.toUpperCase(), font: FONT, size: PT12, bold: true })],
  alignment: AlignmentType.LEFT,
  pageBreakBefore: true,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/**
 * Заголовок подраздела (Полужирный, первая прописная, 12пт, по левому краю)
 * Перед ним — 2 пустые строки (вставляются снаружи), после — 1 пустая строка.
 */
const h2 = (text) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12, bold: true })],
  alignment: AlignmentType.LEFT,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/**
 * Заголовок пункта (Полужирный, 12пт, с отступом)
 */
const h3 = (text) => new Paragraph({
  children: [new TextRun({ text, font: FONT, size: PT12, bold: true })],
  alignment: AlignmentType.LEFT,
  indent: { firstLine: INDENT_FIRST },
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Элемент перечисления (тире + отступ) */
const bullet = (text) => new Paragraph({
  children: [new TextRun({ text: `\u2014 ${text}`, font: FONT, size: PT12 })],
  alignment: AlignmentType.JUSTIFIED,
  indent: { left: INDENT_FIRST, hanging: 0 },
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Подпись к рисунку: «Рис. X.X. Название» — по центру, без точки в конце */
const figCaption = (num, title) => new Paragraph({
  children: [new TextRun({ text: `Рис. ${num}. ${title}`, font: FONT, size: PT12 })],
  alignment: AlignmentType.CENTER,
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
});

/** Строка кода */
const codeLine = (text) => new Paragraph({
  children: [new TextRun({ text, font: 'Courier New', size: PT12 - 2 })],
  alignment: AlignmentType.LEFT,
  indent: { left: INDENT_FIRST },
  spacing: { line: LINE_SINGLE, after: 0, before: 0 },
  shading: { type: ShadingType.SOLID, color: 'F5F5F5' },
});

const code = (lines) => lines.map(l => codeLine(l));

